function range(name) {
  const start = src.indexOf(`const ${name}: TranslationSet = {`);
  const i = order.indexOf(name);
  const next = order[i + 1];
  const end = next
    ? src.indexOf(`const ${next}: TranslationSet = {`, start)
    : src.indexOf("export const TRANSLATIONS", start);
  return { start, end };
}

for (const [base, pairs] of Object.entries(data)) {
  const { start, end } = range(base);
  let block = src.slice(start, end);

  for (const [k, v] of Object.entries(pairs)) {
    const line = `  ${k}: ${JSON.stringify(v)},`;
    const rx = new RegExp(`^\\s{2}${k}:\\s*".*",?\\s*$`, "m");
    if (rx.test(block)) {
      block = block.replace(rx, line);
    } else {
      // Ensure previous property ends with comma before appending.
      block = block.replace(/([^,\s])\n};\s*$/, "$1,\n};");
      block = block.replace(/\n};\s*$/, `\n${line}\n};`);
    }
  }

  src = src.slice(0, start) + block + src.slice(end);
}

